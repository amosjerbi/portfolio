import config from './config.js';

let posts = []; // Global variable to store posts
let currentPostIndex = 0;
let currentPage = 1;

function initTouchGestures() {
    const modal = document.getElementById('modal');
    const modalContent = document.querySelector('.modallic-content');
    const hammer = new Hammer(modalContent);
    
    // Configure horizontal swipe
    hammer.get('swipe').set({ direction: Hammer.DIRECTION_HORIZONTAL });
    
    hammer.on('swipeleft', function() {
        if (currentPostIndex < posts.length - 1) {
            nextPost();
        }
    });
    
    hammer.on('swiperight', function() {
        if (currentPostIndex > 0) {
            prevPost();
        }
    });
}

function showMorePosts() {
    const startIndex = (currentPage - 1) * config.postsPerPage;
    const endIndex = startIndex + config.postsPerPage;
    const postsToShow = posts.slice(startIndex, endIndex);
    
    postsToShow.forEach((post, index) => {
        const div = document.createElement('div');
        div.className = 'flow';

        const img = document.createElement('img');
        img.className = 'img';
        img.src = post.media_url;
        img.alt = post.caption || 'Instagram post';
        img.loading = 'lazy';
        
        // Add click event listener to show the modal
        div.addEventListener('click', () => {
            showPost(startIndex + index);
            document.getElementById('modal').style.display = 'block';
        });

        div.appendChild(img);
        document.getElementById('gallery').appendChild(div);
    });
    
    // Show/hide load more button
    const loadMoreBtn = document.getElementById('loadMoreBtn');
    if (endIndex >= posts.length) {
        loadMoreBtn.style.display = 'none';
    } else {
        loadMoreBtn.style.display = 'block';
    }
}

function showPost(index) {
    if (index >= 0 && index < posts.length) {
        currentPostIndex = index;
        const post = posts[index];
        const modalImg = document.getElementById('modalImage');
        const modalCaption = document.getElementById('modalCaption');
        const modalLink = document.getElementById('modalLink');
        
        modalImg.src = post.media_url;
        modalCaption.textContent = ''; 
        modalLink.href = post.permalink;
        
        // Update navigation buttons visibility
        document.querySelector('.prev-btn').style.display = index === 0 ? 'none' : 'block';
        document.querySelector('.next-btn').style.display = index === posts.length - 1 ? 'none' : 'block';
    }
}

function nextPost() {
    if (currentPostIndex < posts.length - 1) {
        showPost(currentPostIndex + 1);
    }
}

function prevPost() {
    if (currentPostIndex > 0) {
        showPost(currentPostIndex - 1);
    }
}

async function loadInstagramPosts() {
    const gallery = document.getElementById('gallery');
    const loadMoreBtn = document.getElementById('loadMoreBtn');
    const modal = document.getElementById('modal');
    
    if (!gallery) {
        console.error('Gallery element not found');
        return;
    }
    
    gallery.innerHTML = '<div class="loading">Loading posts...</div>';
    
    try {
        // Debug: Log fetch URL
        console.log('Fetching Instagram posts from:', 'assets/data/instagram-posts.json');
        
        // Load the JSON file generated by the GitHub Actions workflow
        const response = await fetch('assets/data/instagram-posts.json', {
            cache: 'no-cache' // Ensure fresh fetch
        });
        
        // Debug: Log response details
        console.log('Fetch response status:', response.status);
        console.log('Fetch response headers:', Object.fromEntries(response.headers.entries()));
        
        if (!response.ok) {
            const errorText = await response.text();
            throw new Error(`HTTP error! status: ${response.status}, body: ${errorText}`);
        }

        const data = await response.json();
        
        // Debug: Log parsed data
        console.log('Parsed Instagram posts data:', data);
        
        gallery.innerHTML = '';

        if (!data.data || data.data.length === 0) {
            console.warn('No Instagram posts found');
            gallery.innerHTML = '<div class="error">No posts found</div>';
            return;
        }

        // Store posts globally and filter media types
        posts = data.data.filter(post => {
            const isAllowedType = config.allowedMediaTypes.includes(post.media_type);
            console.log(`Post ${post.id}: type ${post.media_type}, allowed: ${isAllowedType}`);
            return isAllowedType;
        });

        console.log(`Filtered posts count: ${posts.length}`);

        // Ensure load more button exists before adding event listener
        if (loadMoreBtn) {
            loadMoreBtn.addEventListener('click', () => {
                currentPage++;
                showMorePosts();
            });
        } else {
            console.warn('Load more button not found');
        }

        // Load initial posts
        showMorePosts();

        // Initialize touch gestures
        initTouchGestures();

        // Add modal close functionality
        if (modal) {
            const closeBtn = modal.querySelector('.close');
            if (closeBtn) {
                closeBtn.onclick = () => modal.style.display = 'none';
            } else {
                console.warn('Close button not found in modal');
            }
            
            // Add navigation button functionality
            const prevBtn = modal.querySelector('.prev-btn');
            const nextBtn = modal.querySelector('.next-btn');
            
            if (prevBtn) {
                prevBtn.addEventListener('click', prevPost);
            } else {
                console.warn('Previous button not found');
            }
            
            if (nextBtn) {
                nextBtn.addEventListener('click', nextPost);
            } else {
                console.warn('Next button not found');
            }
            
            window.onclick = (event) => {
                if (event.target === modal) {
                    modal.style.display = 'none';
                }
            };

            // Add keyboard navigation
            document.addEventListener('keydown', (e) => {
                if (modal.style.display === 'block') {
                    if (e.key === 'ArrowLeft') prevPost();
                    if (e.key === 'ArrowRight') nextPost();
                    if (e.key === 'Escape') modal.style.display = 'none';
                }
            });
        } else {
            console.warn('Modal element not found');
        }
    } catch (error) {
        console.error('Detailed error loading Instagram posts:', {
            message: error.message,
            name: error.name,
            stack: error.stack
        });
        
        // Try to fetch and log the raw file contents
        try {
            const rawResponse = await fetch('assets/data/instagram-posts.json');
            const rawText = await rawResponse.text();
            console.error('Raw file contents:', rawText);
        } catch (rawFetchError) {
            console.error('Could not fetch raw file:', rawFetchError);
        }
        
        gallery.innerHTML = `<div class="error">Error loading posts: ${error.message}</div>`;
    }
}

// Initialize when DOM is ready
document.addEventListener('DOMContentLoaded', loadInstagramPosts);
